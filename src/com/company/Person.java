package com.company;

public class Person {

    public int age;
    public double height;
    public double weight;
    public String race;
    public String gender;

    public Person(int age, double height, double weight, String race, String gender) {
        this.age = age;
        this.height = height;
        this.weight = weight;
        this.race = race;
        this.gender = gender;
    }
}
